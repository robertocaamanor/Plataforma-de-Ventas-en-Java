    private void btnIngresarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarClientesActionPerformed
        String sql = "INSERT INTO clientes(nombre, apellido, block, departamento) VALUES (?, ?, ?, ?)";
        try {
            if(txtNombre.getText().length() != 0 || txtApellido.getText().length() != 0 || txtBlock.getText().length() != 0 || txtDepartamento.getText().length() !=0){
                pst = conn.prepareStatement(sql);
                pst.setString(1,txtNombre.getText());
                pst.setString(2,txtApellido.getText());
                pst.setString(3,txtBlock.getText());
                pst.setString(4,txtDepartamento.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
                txtNombre.setText("");
                txtApellido.setText("");
                txtBlock.setText("");
                txtDepartamento.setText("");
                mostrarDatos();
            }else{
                JOptionPane.showMessageDialog(null,"Ingrese datos");
                txtNombre.setText("");
                txtApellido.setText("");
                txtBlock.setText("");
                txtDepartamento.setText("");
                mostrarDatos();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnIngresarClientesActionPerformed

