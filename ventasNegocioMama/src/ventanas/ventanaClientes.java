    void mostrarDatos(){
        DefaultTableModel dm = new DefaultTableModel();
        tClientes = this.tablaClientes;
        tClientes.setModel(dm);
        
        dm.setColumnIdentifiers(new Object[]{"ID","NOMBRE","APELLIDO","BLOCK","DEPARTAMENTO"});
        conexionSql con = new conexionSql();
        rs = con.SeleccionarTodosUsuarios();
        try {
            while(rs.next()){
                dm.addRow(new Object[]{rs.getInt("id"), rs.getString("nombre"), rs.getString("apellido"), rs.getInt("block"), rs.getInt("departamento")});
            }
        } catch (Exception e) {
        }
    }

