    private void btnAgregarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProdActionPerformed
        /*datocombo = cboTipoStock.getSelectedItem().toString();*/
        String sql = "INSERT INTO productos(nombre_producto, tipo_producto, precio_producto, stock_producto) VALUES (?, ?, ?, ?)";
        try {
            if(txtNomProducto.getText().length() != 0 ){
                pst = conn.prepareStatement(sql);
                pst.setString(1,txtNomProducto.getText());
                pst.setString(2,cboTipoStock.getSelectedItem().toString());
                pst.setString(3,txtPrecioProd.getText());
                pst.setInt(4,cboCantStock.getSelectedIndex());
                pst.executeUpdate();
                /*pst.executeBatch();*/
                JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
                txtNomProducto.setText("");
                mostrarDatos();
            }else{
                JOptionPane.showMessageDialog(null,"Ingrese datos");
                txtNomProducto.setText("");
                mostrarDatos();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAgregarProdActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int precio = cboCantStock.getSelectedIndex();
        String precioString = Integer.toString(precio);
        String[] registro = {txtNomProducto.getText(), cboTipoStock.getSelectedItem().toString(), txtPrecioProd.getText(), precioString, id};
        int opcion = conexionSql.actualizarRegistroProductos(registro);
        if(opcion !=0){
            JOptionPane.showMessageDialog(this, "Registro agregado");
            mostrarDatos();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fila = tablaStock.getSelectedRow();
        String cod = "";
        cod = tablaStock.getValueAt(fila, 0).toString();
        try {
            pst = conn.prepareStatement("DELETE FROM productos WHERE id_producto = '"+cod+"'");
            pst.executeUpdate();
            mostrarDatos();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

