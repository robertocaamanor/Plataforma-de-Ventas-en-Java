    void mostrarDatos(){
        DefaultTableModel dm = new DefaultTableModel();
        tClientes = this.tablaStock;
        tClientes.setModel(dm);
        
        dm.setColumnIdentifiers(new Object[]{"ID","PRODUCTO","TIPO","PRECIO","STOCK"});
        conexionSql con = new conexionSql();
        rs = con.SeleccionarProductosStock();
        try {
            while(rs.next()){
                dm.addRow(new Object[]{rs.getString("id_producto"), rs.getString("nombre_producto"), rs.getString("tipo_producto"), rs.getInt("precio_producto"), rs.getInt("stock_producto")});
            }
        } catch (Exception e) {
        }
    }

