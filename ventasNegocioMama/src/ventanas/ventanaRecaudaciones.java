    void mostrarDatos(){
        DefaultTableModel dm = new DefaultTableModel();
        tRecaudaciones = this.tblRecaudaciones;
        tRecaudaciones.setModel(dm);
        
        dm.setColumnIdentifiers(new Object[]{"ID","NOMBRE","PRODUCTO","CANTIDAD","PRECIO"});
        conexionSql con1 = new conexionSql();
        rs2 = con1.SeleccionarTodosVentas();
        try {
            while(rs2.next()){
                dm.addRow(new Object[]{rs2.getString("id_venta"), rs2.getString("nombre_cliente"), rs2.getString("nombre_producto"), rs2.getString("cant_vendida"), rs2.getString("precio_obtenido")});
            }
        } catch (Exception e) {
        }
    }
    
