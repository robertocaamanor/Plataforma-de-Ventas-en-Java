    private void btnEjecutarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEjecutarVentaActionPerformed
        String sql = "INSERT INTO ventas(nombre_cliente, nombre_producto, cant_vendida, precio_obtenido) VALUES (?, ?, ?, ?)";
        try {
            if(txtNombreProducto.getText().length() != 0 ){
                pst = conn.prepareStatement(sql);
                pst.setString(1,txtNombreCliente.getText());
                pst.setString(2,txtNombreProducto.getText());
                pst.setString(3,txtCantidad.getText());
                pst.setString(4,preciototal.getText());
                pst.executeUpdate();
                /*pst.executeBatch();*/
                JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
                txtNombreProducto.setText("");
                txtNombreCliente.setText("");
                txtCantidad.setText("");
                txtPrecioProducto.setText("");
                mostrarDatos();
            }else{
                JOptionPane.showMessageDialog(null,"Ingrese datos");
                txtNombreProducto.setText("");
                txtNombreCliente.setText("");
                txtCantidad.setText("");
                txtPrecioProducto.setText("");
                mostrarDatos();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnEjecutarVentaActionPerformed

